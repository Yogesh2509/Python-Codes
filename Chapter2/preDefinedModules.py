# PRE DEFINED MODULES OF MATHS
import math
print(math.sqrt(253))  # to finf the square root
print(math.ceil(1.4))  # it gives nearest upper value
print(math.pow(2, 3))
print(math.floor(3.6))  # it gives nearest lower value
r = 180/math.pi
print(math.sin(r))
print(math.log(10))
print(math.log10(10))
s = 45*(math.pi)/180
s = math.radians(45)
print(math.tan(s))
print(math.factorial(5))
print(math.gcd(5, 15, 20))  # to find the greatest common divisor
print(math.fmod(18, 4))  # to find the modules
print(math.fabs(-25))  # to convert negative value to positive
print(divmod(5, 2))  # to find the quotient and reminder
print(sum([2, 4, 3, 4]))  # to find the sum of
